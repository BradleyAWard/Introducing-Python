# Code 2
# If statement with multiple options
colour = "yellow"

if colour == "red":
    print("The vegetable is red")
elif colour == "green":
    print("The vegetable is green")
elif colour == "yellow":
    print("The vegetable is yellow")
else:
    print("This probably isn't a vegetable")